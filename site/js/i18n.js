  function tKeyword(key) {
    if (_lang === 'es') {
      if (keywordTranslations[key]) return keywordTranslations[key];
      if (geoTranslations[key]) return geoTranslations[key];
      if (countryTranslations[key]) return countryTranslations[key];
    }
    return key;
  }

  function tGeo(key) {
    if (_lang === 'es' && geoTranslations[key]) {
      return geoTranslations[key];
    }
    return key;
  }

  function tCountry(key) {
    if (_lang === 'es' && countryTranslations[key]) {
      return countryTranslations[key];
    }
    return key;
  }

  function tDiscipline(key) {
    if (_lang === 'es' && disciplineTranslations[key]) {
      return disciplineTranslations[key];
    }
    return key;
  }

  function getLang() {
    return _lang;
  }

  function setLang(lang) {
    _lang = lang;
    localStorage.setItem('chacomap-lang', lang);
  }

  function t(key) {
    return (translations[_lang] && translations[_lang][key])
      || (translations['en'] && translations['en'][key])
      || key;
  }

  /**
   * Apply translations to all elements with data-i18n attribute.
   */
  function applyToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const translated = t(key);
      if (el.tagName === 'INPUT' && el.type !== 'submit') {
        el.placeholder = translated;
      } else {
        el.textContent = translated;
      }
    });
    // Update lang toggle button
    const toggle = document.querySelector('.lang-toggle');
    if (toggle) {
      toggle.textContent = _lang === 'en' ? 'ES' : 'EN';
    }
    document.documentElement.lang = _lang;
  }

  function toggle() {
    setLang(_lang === 'en' ? 'es' : 'en');
    applyToDOM();
  }

  function tTheme(themeKey) {
    return t('theme.' + themeKey);
  }

  return { getLang, setLang, t, tTheme, tKeyword, tGeo, tCountry, tDiscipline, applyToDOM, toggle };
